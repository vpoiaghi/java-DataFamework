package bo;

public class BoNamedItem extends BoItem {
	
	private String name;

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	
	@Override
    public String toString() {
		return getName();
	}

}
